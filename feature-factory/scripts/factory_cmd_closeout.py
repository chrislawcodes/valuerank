@mutates_state("closeout")
def command_standalone_closeout(args: argparse.Namespace) -> int:
    slug = args.slug
    root = workflow_dir(slug)
    state = load_workflow_state(slug)

    # Guard: refuse to run on an empty/no-FF state.
    # "Empty" means there is no meaningful runner activity recorded: no stages
    # were checkpointed, no AI calls were made, and no reviews exist on disk.
    # This indicates the slug was never actually run through the FF runner.
    has_stages = bool(state.get("stages"))
    has_token_usage = bool(state.get("token_usage"))
    reviews_exist = reviews_dir(slug).exists() and any(
        reviews_dir(slug).glob("*.review.md")
    )
    if not (has_stages or has_token_usage or reviews_exist):
        print(
            f"closeout: slug '{slug}' has no runner state (no discovery, stages, or token_usage). "
            "This slug was not run through the FF runner — no closeout record generated.",
            file=sys.stderr,
        )
        return 0

    # PR metadata — use provided args, fall back to gh CLI detection
    pr_url: str = getattr(args, "pr_url", None) or ""
    pr_number_raw = getattr(args, "pr_number", None)
    merge_sha: str = getattr(args, "merge_sha", None) or ""
    operator_note: str = getattr(args, "note", None) or ""
    out_path_raw: str | None = getattr(args, "out", None)

    pr_number: int | None = None
    if pr_number_raw is not None:
        try:
            pr_number = int(pr_number_raw)
        except (TypeError, ValueError):
            pr_number = None

    if not pr_url and not pr_number:
        branch_hint = _slug_to_branch(slug)
        detected = _detect_pr_from_gh(branch_hint)
        if detected:
            pr_url = pr_url or detected.get("pr_url", "")
            pr_number = pr_number or detected.get("pr_number")
            merge_sha = merge_sha or detected.get("merge_sha", "")

    closed_at = _iso_now()

    # Scan review files
    review_map = _scan_reviews(slug)
    token_usage = state.get("token_usage", []) if isinstance(state.get("token_usage"), list) else []
    codex_dispatches = state.get("codex_dispatches", []) if isinstance(state.get("codex_dispatches"), list) else []
    coverage = _build_review_coverage(review_map, token_usage)

    # Build coverage_summary for state.delivery
    coverage_summary: dict = {}
    for stage in ["spec", "plan", "tasks", "diff"]:
        data = coverage.get(stage, {})
        coverage_summary[f"{stage}_lenses_run"] = data.get("lenses_run", [])
        coverage_summary[f"{stage}_lenses_skipped"] = data.get("lenses_skipped", [])

    # Tick confirmed-merged slice checkboxes in tasks.md
    tasks_path = root / "tasks.md"
    _tick_merged_slices(tasks_path)

    # Determine output path
    if out_path_raw:
        out_path = Path(out_path_raw)
    else:
        out_path = root / "closeout.md"

    # Build closeout.md — preserve operator notes section on re-run
    existing_text: str | None = None
    if out_path.exists():
        existing_text = out_path.read_text(encoding="utf-8")

    closeout_text = _compose_closeout_md(
        existing_text,
        slug,
        pr_url,
        pr_number,
        merge_sha,
        closed_at,
        coverage,
        operator_note,
        token_usage=token_usage,
        codex_dispatches=codex_dispatches,
    )
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(closeout_text, encoding="utf-8")

    # Update state.delivery
    delivery_record = {
        "pr_url": pr_url,
        "pr_number": pr_number,
        "merge_sha": merge_sha,
        "closed_at": closed_at,
        "operator_note": operator_note,
        "review_coverage_summary": coverage_summary,
    }
    state[DELIVERY_KEY] = delivery_record
    save_workflow_state(slug, state)

    print(str(out_path))
    return 0
